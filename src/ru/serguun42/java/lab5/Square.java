package ru.serguun42.java.lab5;

public class Square extends Shape {
    Square(int posX, int posY) {
        super(posX, posY);
    }

    @Override
    public void setPosX(int posX) {
        this.posX = posX;
    }

    @Override
    public void setPosY(int posY) {
        this.posY = posY;
    }

    @Override
    public int getPosX() {
        return this.posX;
    }

    @Override
    public int getPosY() {
        return this.posY;
    }
}
